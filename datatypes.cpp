#include <iostream>
using namespace std;
int main()
{
  // we can find the size of any datatype in cpp using sizeof() operators
  cout << "the size of int is" << sizeof(int) << "bytes" << endl;
  cout << "the size of int is" << sizeof(bool) << "bytes" << endl;
  cout << "the size of int is" << sizeof(float) << "bytes" << endl;
  cout << "the size of int is" << sizeof(double) << "bytes" << endl;
  cout << "the size of int is" << sizeof(char) << "bytes" << endl;
  return 0;
}
